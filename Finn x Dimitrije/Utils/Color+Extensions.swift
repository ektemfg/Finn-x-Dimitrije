import SwiftUI

extension Color {
    
    static var yellowColor: Color {
        return Color("standardYellow")
    }
    static var darkerYellowColor: Color {
        return Color("darkYellow")
    }
    static var darkerGreyColor: Color {
        return Color("darkerGray")
    }
    static var brightGreyColor: Color {
        return Color("brightGray")
    }
    static var finnBlue: Color {
        return Color("finnBlue")
    }
    static var lighterBlue: Color {
        return Color("lighterBlue")
    }
}
